class Fish:

    # class attribute
    species = "fish"

    # instance attribute
    def __init__(self, name, age):
        self.name = name
        self.age = age
        self.average_speed = 0
        self.distance_covered = 0
        self.life = 0
        self.skill = 0
        self.attack_stat = 0
        self.defense_skill = 0
        self.armor = 0
        self.weapon = 0
        self.potion = 0
        self.poison = 0

